package com.zhang.myaccessproject;

import android.util.Log;

public class L {
    private static final String TAG = "123456";

    public static void e(String s) {
        Log.e(TAG, "=====" + s + "=====");
    }

    public static void w(String s) {
        Log.w(TAG, "=====" + s + "=====");
    }

    public static void i(String s) {
        Log.i(TAG, "=====" + s + "=====");
    }
}
